def inventory() -> int:
    stack = active_stack()
    try:
        raw = subprocess.check_output(
            ["pulumi", "stack", "output", "ansibleInventory", "--json", "--stack", stack],
            cwd=PULUMI_DIR,
            env=command_env(),
            text=True,
        )
    except subprocess.CalledProcessError as exc:
        message = f"Unable to read Pulumi ansibleInventory output: {exc}"
        print(message, file=sys.stderr)
        record_history("infra inventory", env=stack, result_for_code=1, error=message)
        return 1

    try:
        import yaml
    except ImportError:
        message = "PyYAML is missing. Run: ./vidctl bootstrap"
        print(message, file=sys.stderr)
        record_history("infra inventory", env=stack, result_for_code=1, error=message)
        return 1

    GENERATED_INVENTORY.parent.mkdir(parents=True, exist_ok=True)
    GENERATED_INVENTORY.write_text(
        "# Generated by ./vidctl infra inventory\n" + yaml.safe_dump(json.loads(raw), sort_keys=False),
        encoding="utf-8",
    )
    record_history("infra inventory", env=stack, result="success", exit_code=0)
    return 0


def persist_vm_resolution(
    topology: dict[str, Any],
    env_name: str,
    host: str,
    service: str,
    provider: str,
) -> None:
    """Copy the region/zone/size Pulumi resolved for this VM back into
    topology.toml.

    Originally alibaba-only (spot-search can land on a different region/size
    than requested); generalized to every provider so runtime/images.toml's
    per-(provider, region) golden-image lookup in set_vm_defaults() has a
    real region to key off after the first deploy, for providers whose
    compute module doesn't otherwise persist one. Best-effort: a failure here
    shouldn't fail an otherwise-successful deploy, it just means the next run
    re-resolves the default instead of reusing what got recorded.
    """
    try:
        raw = subprocess.check_output(
            ["pulumi", "stack", "output", "topology", "--json", "--stack", env_name],
            cwd=PULUMI_DIR,
            env=command_env(),
            text=True,
        )
        remote_topology = json.loads(raw)
    except (subprocess.CalledProcessError, json.JSONDecodeError):
        return

    for remote_worker in remote_topology.get("workers", []):
        if (
            remote_worker.get("host") == host
            and remote_worker.get("service") == service
            and remote_worker.get("provider") == provider
            and remote_worker.get("env", env_name) == env_name
        ):
            local_worker = find_worker(topology, env_name, host, service, provider)
            if local_worker is not None:
                if remote_worker.get("region"):
                    local_worker["region"] = remote_worker["region"]
                if remote_worker.get("zone"):
                    local_worker["zone"] = remote_worker["zone"]
                if remote_worker.get("size"):
                    local_worker["size"] = remote_worker["size"]
                if remote_worker.get("resource_id"):
                    local_worker["resource_id"] = remote_worker["resource_id"]
            return


def host_address(host: str) -> str:
    try:
        import yaml
    except ImportError:
        return ""
    if not GENERATED_INVENTORY.exists():
        return ""
    try:
        data = yaml.safe_load(GENERATED_INVENTORY.read_text(encoding="utf-8"))
        host_entry = data["all"]["children"]["xaisen"]["hosts"].get(host, {})
        return str(host_entry.get("ansible_host", ""))
    except (KeyError, TypeError, AttributeError):
        return ""


def bot_control_token() -> str:
    """Read (or generate + persist) BOT_CONTROL_TOKEN from
    secrets/services/bot.env -- the SAME file deploy_one_service.yml's
    generic per-service secrets mechanism already copies into the bot
    container as its env_file (see "Copy per-service secrets file to host"),
    so this is the single source of truth for the token on both ends: the
    server reads it from the container's env, and sync_bot_frontend_env()
    below reads this same file to push the matching value into the
    frontends' VITE_BOT_CONTROL_TOKEN. Generated once, on first use."""
    path = SERVICE_SECRETS_DIR / "bot.env"
    values = read_env_file(path)
    token = values.get("BOT_CONTROL_TOKEN", "")
    if token:
        return token
    token = py_secrets.token_urlsafe(32)
    values["BOT_CONTROL_TOKEN"] = token
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(
        "\n".join(f"{key}={value}" for key, value in values.items()) + "\n",
        encoding="utf-8",
    )
    return token


def sync_bot_frontend_env(scenario: dict[str, Any]) -> bool:
    """Mirror of contract.sync_frontend_env(), but for the bot control
    server's URL/token instead of contract object IDs -- bot has no
    on-chain registry (test/demo tool only, see apps/bot/README.md), so
    relay/signaling-style on-chain endpoint discovery isn't an option; the
    admin dashboard needs a static VITE_BOT_CONTROL_URL/TOKEN instead.

    Only writes when the scenario actually declares a `service = "bot"`
    worker AND that worker's host has a resolved address (i.e. infra.control()/
    control_many() already ran this apply -- call this AFTER the topology
    reconcile loop, not before). Returns True if either value actually
    changed, so the caller knows whether the frontend needs rebuilding --
    Vite bakes VITE_* in at build time, so a stale build would keep pointing
    at the old value even after this writes the new one to disk.
    """
    workers = scenario.get("workers", [])
    bot_worker = next((w for w in workers if w.get("service") == "bot"), None)
    if bot_worker is None:
        return False
    address = host_address(str(bot_worker.get("host", "")))
    if not address:
        return False
    url = f"https://bot.{address.replace('.', '-')}.sslip.io"
    token = bot_control_token()
    mapping = {"VITE_BOT_CONTROL_URL": url, "VITE_BOT_CONTROL_TOKEN": token}
    changed = False
    for path in (CLIENT_ENV_PATH, ADMIN_ENV_PATH):
        before = read_env_file(path)
        was_current = before.get("VITE_BOT_CONTROL_URL") == url and before.get("VITE_BOT_CONTROL_TOKEN") == token
        if sync_env_keys(path, mapping):
            print(f"Synced bot control URL -> {path}")
            if not was_current:
                changed = True
    return changed


def registry_status(host: str, worker_key: str, address: str) -> str:
    key_path = SSH_KEY_ROOT / host / "id_ed25519"
    if not address or not key_path.exists():
        return "unknown (host unreachable)"
    container_name = f"xaisen-{worker_key}"
    try:
        result = subprocess.run(
            [
                "ssh",
                "-o", "StrictHostKeyChecking=no",
                "-o", "ConnectTimeout=8",
                "-i", str(key_path),
                f"root@{address}",
                f"docker logs {container_name} 2>&1 | grep -E 'operator address|node_id=|bootstrap failed' | tail -40",
            ],
            capture_output=True,
            text=True,
            timeout=20,
            check=False,
        )
    except (subprocess.TimeoutExpired, OSError) as exc:
        return f"unknown ({exc})"

    output = result.stdout.strip()
    if not output:
        return "unknown (no operator log lines found)"

    # docker logs preserves history across `docker restart` (same container),
    # so only consider lines from the most recent boot's operator address block.
    lines = output.splitlines()
    last_boot_start = max(
        (i for i, line in enumerate(lines) if "operator address" in line),
        default=0,
    )
    lines = lines[last_boot_start:]

    if any("bootstrap failed" in line for line in lines):
        error_line = next((line for line in lines if "bootstrap failed" in line), "")
        return f"not registered ({error_line.split('Error:', 1)[-1].strip() or 'see logs'})"
    node_id_line = next(
        (line for line in reversed(lines) if "node_id=" in line),
        "",
    )
    if node_id_line:
        node_id = node_id_line.split("node_id=", 1)[-1].split(";")[0].strip()
        return f"registered (node_id={node_id})"
    return "unknown (unrecognized log output)"


