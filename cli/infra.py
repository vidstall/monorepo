from __future__ import annotations

import json
import subprocess
import sys

from .context import ANSIBLE_DIR, GENERATED_INVENTORY, PULUMI_DIR, ROOT, command_env, run, venv_bin


def pulumi_stack() -> str:
    return command_env().get("PULUMI_STACK", "dev")


def init() -> int:
    env = command_env()
    stack = env.get("PULUMI_STACK", "dev")
    selected = subprocess.run(
        ["pulumi", "stack", "select", stack],
        cwd=PULUMI_DIR,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if selected.returncode == 0:
        print(f"Selected Pulumi stack {stack}")
        return 0
    return run(["pulumi", "stack", "init", stack], cwd=PULUMI_DIR, env=env)


def preview() -> int:
    return run(["pulumi", "preview", "--stack", pulumi_stack()], cwd=PULUMI_DIR)


def apply(yes: bool) -> int:
    if not yes:
        print("Refusing to apply infrastructure without --yes.", file=sys.stderr)
        return 2
    code = run(["pulumi", "up", "--yes", "--stack", pulumi_stack()], cwd=PULUMI_DIR)
    if code != 0:
        return code
    return inventory()


def inventory() -> int:
    env = command_env()
    raw = subprocess.check_output(
        ["pulumi", "stack", "output", "ansibleInventory", "--json", "--stack", env.get("PULUMI_STACK", "dev")],
        cwd=PULUMI_DIR,
        env=env,
        text=True,
    )

    try:
        import yaml
    except ImportError:
        print("PyYAML is missing. Run: ./vidctl bootstrap", file=sys.stderr)
        return 1

    GENERATED_INVENTORY.parent.mkdir(parents=True, exist_ok=True)
    GENERATED_INVENTORY.write_text(
        "# Generated by ./vidctl infra inventory\n"
        + yaml.safe_dump(json.loads(raw), sort_keys=False),
    )
    print(f"Wrote {GENERATED_INVENTORY.relative_to(ROOT)}")
    return 0


def ansible_inventory() -> int:
    executable = venv_bin("ansible-inventory")
    if not executable.exists():
        print("Ansible is missing. Run: ./vidctl bootstrap", file=sys.stderr)
        return 1
    return run([executable, "--list"], cwd=ANSIBLE_DIR)


def ping() -> int:
    return ansible_playbook("ping.yml")


def configure() -> int:
    return ansible_playbook("site.yml")


def deploy(yes: bool) -> int:
    if not yes:
        print("Refusing to deploy infrastructure without --yes.", file=sys.stderr)
        return 2
    code = apply(yes=True)
    if code != 0:
        return code
    return configure()


def ansible_playbook(playbook: str) -> int:
    executable = venv_bin("ansible-playbook")
    if not executable.exists():
        print("Ansible is missing. Run: ./vidctl bootstrap", file=sys.stderr)
        return 1
    return run([executable, f"playbooks/{playbook}"], cwd=ANSIBLE_DIR)
