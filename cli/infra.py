def configure(
    host_limit: str | None = None,
    container_state: str = "started",
    extra_vars: dict[str, Any] | None = None,
) -> int:
    all_extra_vars = docker_deploy_extra_vars()
    all_extra_vars["xaisen_container_state"] = container_state
    if extra_vars:
        all_extra_vars.update(extra_vars)
    code = ansible_playbook("site.yml", extra_vars=all_extra_vars, host_limit=host_limit)
    record_history("infra configure", env=active_stack(), result_for_code=code)
    return code


def docker_deploy_extra_vars() -> dict[str, Any]:
    from . import registry

    try:
        state = registry.read_runtime_registry()
    except ValueError as exc:
        print(f"Skipping docker image deployment: {exc}", file=sys.stderr)
        return {}

    extra_vars: dict[str, Any] = {
        "xaisen_images": state.images,
        "xaisen_tags": state.deployed,
        "xaisen_registry_host": state.host,
    }
    try:
        config = registry.provider_config(state.provider, require_credentials=True)
    except ValueError as exc:
        print(f"Skipping registry login: {exc}", file=sys.stderr)
        return extra_vars
    extra_vars["xaisen_registry_username"] = config.username
    extra_vars["xaisen_registry_password"] = config.password
    return extra_vars


def bootstrap_media(name: str, env_name: str, wallet_entry: dict[str, Any]) -> dict[str, str]:
    """Idempotently registers a media instance's operator wallet on-chain
    (worker + ROLE_SFU + media cluster), then returns the Ansible extra_vars
    needed to actually run it: XAISEN_CLUSTER_ID, XAISEN_LIVEKIT_URL,
    LIVEKIT_KEYS, and the broker's own x25519 key file content. The one-time
    registration steps no-op if wallet_entry already has a cluster_id from a
    previous run, but the on-chain node profile (broker endpoint/public_url)
    is always republished, since it can change across restarts."""
    from . import media_bootstrap, wallet

    address = instance_address(name)
    if not address:
        raise RuntimeError(f"could not resolve public IP for {name}; run inventory first")
    public_url = f"https://{address}.sslip.io"

    contract_path = contract_env_path(env_name)
    contract_values = read_env_file(contract_path)
    package_id = contract_values.get("CONTRACT_PACKAGE_ID", "")
    original_package_id = contract_values.get("CONTRACT_ORIGINAL_PACKAGE_ID", "") or package_id
    registry_id = contract_values.get("CONTRACT_REGISTRY_OBJECT_ID", "")
    if not package_id or not registry_id:
        raise RuntimeError(f"{contract_path} is missing CONTRACT_PACKAGE_ID/CONTRACT_REGISTRY_OBJECT_ID")

    # Re-fetch media's entry from a fresh read so the object we mutate below
    # is the same one that gets persisted by write_wallets (wallet_entry, as
    # passed in from control(), came from ensure_wallet()'s own separate
    # read/write cycle and is not part of this `wallets` dict).
    wallets = wallet.read_wallets()
    media_entry = wallet.find_wallet(wallets, name, env_name)
    if media_entry is None:
        raise RuntimeError(f"wallet entry for {name} disappeared between ensure_wallet and bootstrap_media")
    routes_entry = wallet.find_wallet_by_service(wallets, "routes", env_name)

    try:
        media_bootstrap.ensure_media_registered(
            media_entry, routes_entry, env_name, package_id, original_package_id, registry_id, public_url,
        )
    finally:
        # Persist whatever progress was made (e.g. node_id from a successful
        # register_worker) even if a later step raised, so a retry can pick
        # up where it left off instead of re-registering from scratch.
        wallet.write_wallets(wallets)
    wallet_entry.update(media_entry)

    return {
        "xaisen_cluster_id": str(media_entry["cluster_id"]),
        "xaisen_media_public_url": public_url,
        "xaisen_media_broker_key": wallet.media_broker_key_content(media_entry),
        "xaisen_livekit_keys": wallet.livekit_keys_env_value(media_entry),
    }


def deploy(yes: bool) -> int:
    if not yes:
        message = "Refusing to deploy infrastructure without --yes."
        print(message, file=sys.stderr)
        record_history("infra deploy", env=active_stack(), result_for_code=2, error=message)
        return 2
    code = apply(yes=True)
    if code == 0:
        code = configure()
    record_history("infra deploy", env=active_stack(), result_for_code=code)
    return code


def control(
    action: str,
    name: str,
    service: str,
    provider: str,
    yes: bool = False,
    find_instance_type: bool = False,
    all_region: bool = False,
) -> int:
    if service not in DOCKER_SERVICES:
        print(f"Unknown service: {service}", file=sys.stderr)
        return 2
    if provider not in PROVIDERS:
        print(f"Unknown provider: {provider}", file=sys.stderr)
        return 2

    topology = read_topology()
    env_name = validate_network(str(topology.get("active_env", "devnet")))
    contract_path = contract_env_path(env_name)
    backend = service_backend(service)

    if backend == "vm" and action in {"pause", "restart"} and provider != "alibaba":
        message = (
            f"{action} is not yet supported for {provider} VM services; "
            "powered lifecycle support currently requires --provider alibaba."
        )
        print(message, file=sys.stderr)
        record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
        return 1

    if action in {"start", "restart"}:
        missing_keys = missing_contract_keys(contract_path)
        if missing_keys:
            message = (
                f"{contract_path} is missing required contract keys: {', '.join(missing_keys)}. "
                f"Run ./vidctl contract publish --env {env_name} --yes first."
            )
            print(message, file=sys.stderr)
            record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
            return 1
        if backend == "vm":
            if provider == "cloudflare":
                message = "cloudflare has no compute/VM product; choose another --provider for vm-backed services."
                print(message, file=sys.stderr)
                record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
                return 1
            if provider == "tencent":
                message = (
                    "tencent VM provisioning is not yet supported (no working Pulumi Python SDK path was "
                    "found for Tencent compute/VPC resources); choose another --provider for now."
                )
                print(message, file=sys.stderr)
                record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
                return 1
            missing_provider_keys = missing_vm_provider_keys(provider)
            if missing_provider_keys:
                message = vm_provider_error(provider, missing_provider_keys)
                print(message, file=sys.stderr)
                record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
                return 1

    if action == "kill" and not yes:
        message = "Refusing to delete infrastructure without --yes."
        print(message, file=sys.stderr)
        record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=2, error=message)
        return 2

    wallet_entry: dict[str, Any] | None = None
    wallet_created = False
    if backend == "vm" and action in {"start", "restart"}:
        from . import wallet

        try:
            wallet_entry, wallet_created = wallet.ensure_wallet(name, service, env_name)
        except (subprocess.CalledProcessError, RuntimeError, KeyError, json.JSONDecodeError) as exc:
            message = f"Failed to create/load operator wallet for {name}: {exc}"
            print(message, file=sys.stderr)
            record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
            return 1

    instance = find_instance(topology, env_name, name, service, provider)
    if instance is None:
        instance = new_instance(env_name, name, service, provider)
        topology.setdefault("instances", []).append(instance)

    previous = str(instance.get("desired_state", instance.get("last_status", "unknown")))
    next_state = desired_state_for(action)
    instance["backend"] = backend
    instance["desired_state"] = next_state
    instance["last_operation"] = action
    instance["last_updated"] = timestamp()
    instance["contract_env"] = relative_contract_env(env_name)
    if backend == "vm" and action in {"start", "restart"}:
        set_vm_defaults(instance, topology, find_instance_type=find_instance_type)
    write_topology(topology)

    code = 0
    failed_stage = "pulumi"
    if code == 0:
        failed_stage = "pulumi"
        if backend == "vm" and provider == "alibaba":
            # An --all-region search may create ad-hoc region-probe provider
            # resources that aren't in the fixed target URN list, so fall back
            # to an untargeted apply whenever that scan is in play. Likewise,
            # `--target` only accepts URNs that already exist in the stack's
            # state, so a brand-new instance (nothing created yet) must also
            # go through an untargeted apply.
            target_urns = alibaba_vm_target_urns(env_name, name, service, bool(instance.get("port")))
            use_targets = not all_region and stack_has_urns(env_name, target_urns)
            code = pulumi_up(
                env_name,
                targets=target_urns if use_targets else None,
                extra_env={"ALIBABA_SCAN_ALL_REGIONS": "1" if all_region else "0"},
            )
        else:
            code = pulumi_up(env_name)

    if code == 0:
        if action == "kill":
            topology["instances"] = [
                item
                for item in topology.get("instances", [])
                if not (
                    item.get("name") == name
                    and item.get("service") == service
                    and item.get("provider") == provider
                    and item.get("env", env_name) == env_name
                )
            ]
            if backend == "vm":
                shutil.rmtree(SSH_KEY_ROOT / name, ignore_errors=True)
        elif backend == "vm" and action in {"start", "restart"}:
            failed_stage = "inventory"
            code = inventory()
            if code == 0 and provider == "alibaba":
                persist_alibaba_vm_resolution(topology, env_name, name, service, provider)
            if code == 0:
                failed_stage = "configure"
                container_state = "restarted" if action == "restart" else "started"
                configure_extra_vars = (
                    {"xaisen_operator_wallet_json": wallet.operator_state_json(wallet_entry)}
                    if wallet_entry is not None
                    else None
                )
                if service == "media" and wallet_entry is not None:
                    failed_stage = "media_bootstrap"
                    try:
                        media_vars = bootstrap_media(name, env_name, wallet_entry)
                        configure_extra_vars = {**(configure_extra_vars or {}), **media_vars}
                    except (RuntimeError, subprocess.CalledProcessError, OSError) as exc:
                        message = f"Media on-chain bootstrap failed for {name}: {exc}"
                        print(message, file=sys.stderr)
                        record_history(action, env=env_name, name=name, service=service, provider=provider, result_for_code=1, error=message)
                        return 1
                    failed_stage = "configure"
                code = configure(host_limit=name, container_state=container_state, extra_vars=configure_extra_vars)

        if code == 0 and action != "kill":
            instance["last_status"] = next_state
            instance["last_error"] = ""
        write_topology(topology)

    if code != 0:
        if failed_stage == "pulumi":
            instance["desired_state"] = previous
        instance["last_error"] = f"{failed_stage} failed with exit code {code}"
        write_topology(topology)

    if code == 0 and backend == "vm" and action in {"start", "restart"} and wallet_entry is not None:
        address = instance_address(name)
        wallet_address = str(wallet_entry.get("address", ""))
        try:
            balance_mist = wallet.current_balance_mist(wallet_address)
        except (subprocess.CalledProcessError, json.JSONDecodeError):
            balance_mist = int(wallet_entry.get("last_balance_mist", 0))
        print(f"IP:      {address or 'unknown'}")
        print(f"Wallet:  {wallet_address[:8]}...")
        print(f"Balance: {balance_mist / 1_000_000_000:.4f} SUI")
        print(f"Registry: {registry_status(name, service, address, env_name)}")

    record_history(
        action,
        env=env_name,
        name=name,
        service=service,
        provider=str(instance.get("provider", "")),
        resource_id=str(instance.get("resource_id", "")),
        previous_status=previous,
        next_status=str(instance.get("desired_state", "")),
        result_for_code=code,
        error=str(instance.get("last_error", "")),
    )
    return code


def ansible_playbook(
    playbook: str,
    extra_vars: dict[str, Any] | None = None,
    host_limit: str | None = None,
) -> int:
    executable = venv_bin("ansible-playbook")
    if not executable.exists():
        print("Ansible is missing. Run: ./vidctl bootstrap", file=sys.stderr)
        return 1
    args = [executable, f"playbooks/{playbook}"]
    if host_limit:
        args += ["--limit", host_limit]
    if extra_vars:
        args += ["--extra-vars", json.dumps(extra_vars)]
    return run(args, cwd=ANSIBLE_DIR)


def select_or_create_stack(stack: str) -> int:
    env = command_env()
    env["PULUMI_STACK"] = stack
    selected = subprocess.run(
        ["pulumi", "stack", "select", stack],
        cwd=PULUMI_DIR,
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if selected.returncode == 0:
        print(f"Selected Pulumi stack {stack}")
        return 0
    return run(["pulumi", "stack", "init", stack], cwd=PULUMI_DIR, env=env)


def pulumi_up(
    stack: str,
    parallel: int | None = None,
    targets: list[str] | None = None,
    extra_env: dict[str, str] | None = None,
) -> int:
    env = command_env()
    env["PULUMI_STACK"] = stack
    if extra_env:
        env.update(extra_env)
    args = ["pulumi", "up", "--yes", "--stack", stack]
    for target in targets or []:
        args.extend(["--target", target])
    if parallel is not None:
        args.extend(["--parallel", str(parallel)])
    return run(args, cwd=PULUMI_DIR, env=env)


def stack_has_urns(stack: str, urns: list[str]) -> bool:
    try:
        raw = subprocess.check_output(
            ["pulumi", "stack", "export", "--stack", stack],
            cwd=PULUMI_DIR,
            env=command_env(),
            text=True,
        )
        existing = {res.get("urn") for res in json.loads(raw).get("deployment", {}).get("resources", [])}
    except (subprocess.CalledProcessError, json.JSONDecodeError):
        return False
    return all(urn in existing for urn in urns)


def alibaba_vm_target_urns(
    stack: str,
    name: str,
    service: str,
    has_service_port: bool,
) -> list[str]:
    prefix = f"urn:pulumi:{stack}::xaisen-iac::"
    resources = [
        ("pulumi:providers:alicloud", f"{name}-vm-provider"),
        ("alicloud:vpc/network:Network", f"{name}-vm-vpc"),
        ("alicloud:vpc/switch:Switch", f"{name}-vm-vswitch"),
        ("alicloud:ecs/keyPair:KeyPair", f"{name}-vm-key"),
        ("alicloud:ecs/securityGroup:SecurityGroup", f"{name}-vm-sg"),
        ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-ssh"),
        ("alicloud:ecs/instance:Instance", f"{name}-vm"),
    ]
    if service in ("routes", "frontend"):
        resources.extend(
            [
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-http"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-https"),
            ]
        )
    elif service == "media":
        resources.extend(
            [
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-http"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-https"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-signal"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-ice"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-broker"),
                ("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-rtc"),
            ]
        )
    elif has_service_port:
        resources.append(("alicloud:ecs/securityGroupRule:SecurityGroupRule", f"{name}-vm-sg-port"))
    return [f"{prefix}{resource_type}::{resource_name}" for resource_type, resource_name in resources]


def active_stack() -> str:
    if RUNTIME_TOPOLOGY_TOML.exists():
        return validate_network(str(read_topology().get("active_env", "devnet")))
    stack = command_env().get("PULUMI_STACK", "devnet")
    return stack if stack in NETWORKS else "devnet"


def ensure_topology(env_name: str) -> dict[str, Any]:
    topology = read_topology() if RUNTIME_TOPOLOGY_TOML.exists() else default_topology(env_name)
    topology["active_env"] = env_name
    topology["contract_env"] = relative_contract_env(env_name)
    topology.setdefault("providers", {provider: {"enabled": False} for provider in PROVIDERS})
    for provider in PROVIDERS:
        topology["providers"].setdefault(provider, {"enabled": False})
    topology.setdefault("instances", [])
    topology.setdefault("objects", [])
    for instance in topology["instances"]:
        instance.setdefault("backend", service_backend(str(instance.get("service", ""))))
    write_topology(topology)
    return topology


def read_topology() -> dict[str, Any]:
    data = tomllib.loads(RUNTIME_TOPOLOGY_TOML.read_text(encoding="utf-8"))
    data.setdefault("instances", [])
    data.setdefault("objects", [])
    data.setdefault("providers", {provider: {"enabled": False} for provider in PROVIDERS})
    for provider in PROVIDERS:
        data["providers"].setdefault(provider, {"enabled": False})
    return data


def write_topology(topology: dict[str, Any]) -> None:
    RUNTIME_TOPOLOGY_TOML.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        f'active_env = "{topology.get("active_env", "devnet")}"',
        f'contract_env = "{topology.get("contract_env", relative_contract_env("devnet"))}"',
        "",
    ]
    providers = topology.get("providers", {})
    for provider in PROVIDERS:
        values = providers.get(provider, {"enabled": False}) if isinstance(providers, dict) else {"enabled": False}
        lines.append(f"[providers.{provider}]")
        for key, value in values.items():
            lines.append(f"{key} = {toml_value(value)}")
        lines.append("")
    for instance in topology.get("instances", []):
        lines.append("[[instances]]")
        for key, value in instance.items():
            lines.append(f"{key} = {toml_value(value)}")
        lines.append("")
    for obj in topology.get("objects", []):
        lines.append("[[objects]]")
        for key, value in obj.items():
            lines.append(f"{key} = {toml_value(value)}")
        lines.append("")
    RUNTIME_TOPOLOGY_TOML.write_text("\n".join(lines), encoding="utf-8")


def find_instance(
    topology: dict[str, Any],
    env_name: str,
    name: str,
    service: str,
    provider: str,
) -> dict[str, Any] | None:
    for instance in topology.get("instances", []):
        if (
            instance.get("name") == name
            and instance.get("service") == service
            and instance.get("provider") == provider
            and instance.get("env", env_name) == env_name
        ):
            return instance
    return None


def find_pinned_alibaba_instance(topology: dict[str, Any], service: str, provider: str) -> dict[str, Any] | None:
    for item in topology.get("instances", []):
        if item.get("service") == service and item.get("provider") == provider and item.get("region") and item.get("size"):
            return item
    return None


