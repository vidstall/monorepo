def apply(yes: bool) -> int:
    stack = active_stack()
    if not yes:
        code = 2
        message = "Refusing to apply infrastructure without --yes."
        print(message, file=sys.stderr)
        record_history("infra apply", env=stack, result_for_code=code, error=message)
        return code

    code = pulumi_up(stack)
    if code == 0:
        code = inventory()
    record_history("infra apply", env=stack, result_for_code=code)
    return code


def inventory() -> int:
    stack = active_stack()
    try:
        raw = subprocess.check_output(
            ["pulumi", "stack", "output", "ansibleInventory", "--json", "--stack", stack],
            cwd=PULUMI_DIR,
            env=command_env(),
            text=True,
        )
    except subprocess.CalledProcessError as exc:
        message = f"Unable to read Pulumi ansibleInventory output: {exc}"
        print(message, file=sys.stderr)
        record_history("infra inventory", env=stack, result_for_code=1, error=message)
        return 1

    try:
        import yaml
    except ImportError:
        message = "PyYAML is missing. Run: ./vidctl bootstrap"
        print(message, file=sys.stderr)
        record_history("infra inventory", env=stack, result_for_code=1, error=message)
        return 1

    GENERATED_INVENTORY.parent.mkdir(parents=True, exist_ok=True)
    GENERATED_INVENTORY.write_text(
        "# Generated by ./vidctl infra inventory\n" + yaml.safe_dump(json.loads(raw), sort_keys=False),
        encoding="utf-8",
    )
    record_history("infra inventory", env=stack, result="success", exit_code=0)
    return 0


def persist_alibaba_vm_resolution(
    topology: dict[str, Any],
    env_name: str,
    name: str,
    service: str,
    provider: str,
) -> None:
    """Copy the region/size Pulumi resolved for this VM back into topology.toml.

    Best-effort: a failure here shouldn't fail an otherwise-successful deploy,
    it just means the next run re-searches instead of reusing the pin.
    """
    try:
        raw = subprocess.check_output(
            ["pulumi", "stack", "output", "topology", "--json", "--stack", env_name],
            cwd=PULUMI_DIR,
            env=command_env(),
            text=True,
        )
        remote_topology = json.loads(raw)
    except (subprocess.CalledProcessError, json.JSONDecodeError):
        return

    for remote_instance in remote_topology.get("instances", []):
        if (
            remote_instance.get("name") == name
            and remote_instance.get("service") == service
            and remote_instance.get("provider") == provider
            and remote_instance.get("env", env_name) == env_name
        ):
            local_instance = find_instance(topology, env_name, name, service, provider)
            if local_instance is not None:
                if remote_instance.get("region"):
                    local_instance["region"] = remote_instance["region"]
                if remote_instance.get("size"):
                    local_instance["size"] = remote_instance["size"]
            return


def instance_address(name: str) -> str:
    try:
        import yaml
    except ImportError:
        return ""
    if not GENERATED_INVENTORY.exists():
        return ""
    try:
        data = yaml.safe_load(GENERATED_INVENTORY.read_text(encoding="utf-8"))
        host = data["all"]["children"]["xaisen"]["hosts"].get(name, {})
        return str(host.get("ansible_host", ""))
    except (KeyError, TypeError, AttributeError):
        return ""


def registry_status(name: str, service: str, address: str, env_name: str) -> str:
    if service == "media":
        # media's on-chain bootstrap runs on the orchestrator via
        # media_bootstrap.py, not inside the container, so it never logs
        # "operator address"/"node_id=" lines the way routes' own
        # self-registration TypeScript does. Read wallet.toml instead.
        from . import wallet as wallet_module

        entry = wallet_module.find_wallet(wallet_module.read_wallets(), name, env_name)
        if entry is None:
            return "unknown (no wallet entry found)"
        cluster_id = entry.get("cluster_id")
        node_id = entry.get("node_id")
        if cluster_id:
            return f"registered (node_id={node_id} cluster_id={cluster_id})"
        if node_id:
            return f"not registered (node_id={node_id}, SFU role/cluster pending)"
        return "not registered"

    key_path = SSH_KEY_ROOT / name / "id_ed25519"
    if not address or not key_path.exists():
        return "unknown (host unreachable)"
    container_name = f"xaisen-{service}"
    try:
        result = subprocess.run(
            [
                "ssh",
                "-o", "StrictHostKeyChecking=no",
                "-o", "ConnectTimeout=8",
                "-i", str(key_path),
                f"root@{address}",
                f"docker logs {container_name} 2>&1 | grep -E 'operator address|node_id=|bootstrap failed' | tail -40",
            ],
            capture_output=True,
            text=True,
            timeout=20,
            check=False,
        )
    except (subprocess.TimeoutExpired, OSError) as exc:
        return f"unknown ({exc})"

    output = result.stdout.strip()
    if not output:
        return "unknown (no operator log lines found)"

    # docker logs preserves history across `docker restart` (same container),
    # so only consider lines from the most recent boot's operator address block.
    lines = output.splitlines()
    last_boot_start = max(
        (i for i, line in enumerate(lines) if "operator address" in line),
        default=0,
    )
    lines = lines[last_boot_start:]

    if any("bootstrap failed" in line for line in lines):
        error_line = next((line for line in lines if "bootstrap failed" in line), "")
        return f"not registered ({error_line.split('Error:', 1)[-1].strip() or 'see logs'})"
    node_id_line = next(
        (line for line in reversed(lines) if "node_id=" in line),
        "",
    )
    if node_id_line:
        node_id = node_id_line.split("node_id=", 1)[-1].split(";")[0].strip()
        return f"registered (node_id={node_id})"
    return "unknown (unrecognized log output)"


